    private void limparCampos(){
        radioConcluido.setSelected(true);
        String caminho = "src/br/com/SoftwarePR/AgendaDeTarefas/Icones/Check_Concluido_Verde.png";
        ImageIcon iconUnchecked = new ImageIcon(caminho); 
        radioConcluido.setIcon(iconUnchecked);
        
        radioNaoConcluido.setSelected(false);
        radioNaoConcluido.setIcon(null);
        
        txtJNome.setText("");
        txtDescricao.setText("");
        dataEntrega.setDateFormatString("");
        
    }
    
    
    private void verificarCheckBox(){
        if(radioConcluido.isSelected() && !radioNaoConcluido.isSelected()){
           String caminho = "src/br/com/SoftwarePR/AgendaDeTarefas/Icones/Check_Concluido_Verde.png";
           ImageIcon iconUnchecked = new ImageIcon(caminho); 
           radioConcluido.setIcon(iconUnchecked);
           radioNaoConcluido.setSelected(false);
           radioNaoConcluido.setIcon(null);
       }else if(!radioConcluido.isSelected() && radioNaoConcluido.isSelected()){
           String caminho = "src/br/com/SoftwarePR/AgendaDeTarefas/Icones/Check_NaoConcluido_Vermelho.png";
           ImageIcon iconUnchecked = new ImageIcon(caminho); 
           radioNaoConcluido.setIcon(iconUnchecked);
           radioConcluido.setSelected(false);
           radioConcluido.setIcon(null);
       }
    }    

    public void receberDados(String id,String nome, String descricao,String data, String Status) throws ParseException{
        
        if(Status.equalsIgnoreCase("1")){
          
           
           String caminho = "src/br/com/SoftwarePR/AgendaDeTarefas/Icones/Check_NaoConcluido.png";
           ImageIcon iconUnchecked = new ImageIcon(caminho); 
           radioNaoConcluido.setIcon(iconUnchecked);
           radioConcluido.setSelected(false);
           radioConcluido.setIcon(null);
        }else{
            
            
           String caminho = "src/br/com/SoftwarePR/AgendaDeTarefas/Icones/Check_Concluido.png";
           ImageIcon iconUnchecked = new ImageIcon(caminho); 
           radioConcluido.setIcon(iconUnchecked);
           radioNaoConcluido.setSelected(false);
           radioNaoConcluido.setIcon(null);
           
            
        }
        txtId.setText(id);
        txtJNome.setText(nome);
        txtDescricao.setText(descricao);
        SimpleDateFormat dataFormatada = new SimpleDateFormat("dd/MM/yyyy");
        Date date = dataFormatada.parse(data);
        dataEntrega.setDate(date);
        
        
        
        
        
    }

