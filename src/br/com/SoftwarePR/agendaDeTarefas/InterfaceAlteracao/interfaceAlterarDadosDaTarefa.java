    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed

        try{
            if(radioConcluido.isSelected()){
                STATUS =0;
            }else{
                STATUS = 1;
            }
            ID= Integer.parseInt(txtId.getText());
            NOME = txtJNome.getText();
            DESCRICAO = txtDescricao.getText();

            Date dataSelecionada = dataEntrega.getDate();

            if(dataSelecionada != null){
                SimpleDateFormat dataFormatada = new SimpleDateFormat("yyyy/MM/dd");
                DATA = dataFormatada.format(dataSelecionada);
            }else{
                DATA = "1999/01/01";
            }

            construtorCriarTarefa Tarefa = new construtorCriarTarefa();

            Tarefa.setStatus(STATUS);
            Tarefa.setNome(NOME);
            Tarefa.setDescricao(DESCRICAO);
            Tarefa.setData(DATA);
            Tarefa.setId(ID);

            conexaoBanco Banco = new conexaoBanco();
            Banco.AlterarTarefa(Tarefa);
            limparCampos();
            
            dispose();

        }catch (NumberFormatException | SQLException e){
            JOptionPane.showMessageDialog(null,"Error: "+e.getMessage(),"ERROR",JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnAlterarActionPerformed

