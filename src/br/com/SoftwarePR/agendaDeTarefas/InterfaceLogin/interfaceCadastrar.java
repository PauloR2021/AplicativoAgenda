    private void btnMostarSenhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostarSenhaActionPerformed
        if(btnMostarSenha.isSelected()){
            txtSenha.setEchoChar((char) 0);
            txtRepetirSenha.setEchoChar((char) 0);
        }
        else{
            txtSenha.setEchoChar('*');
            txtRepetirSenha.setEchoChar('*');
            
        }
    }//GEN-LAST:event_btnMostarSenhaActionPerformed

