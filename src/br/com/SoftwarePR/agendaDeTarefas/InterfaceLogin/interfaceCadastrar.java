    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try{
        
            conexaoBanco Banco = new conexaoBanco();
            construtorCadastro cadastro = new construtorCadastro();

            NOME = txtNome.getText();
            USUARIO = txtUsuario.getText();
            SENHA = txtSenha.getText();
            NOVASENHA = txtRepetirSenha.getText();
            CPF = txtCpf.getText();
            EMAIL = txtEmail.getText();

            if(SENHA.contentEquals(NOVASENHA)){
              try{
                    cadastro.setNome(NOME);
                    cadastro.setUsuario(USUARIO);
                    cadastro.setSenha(SENHA);
                    cadastro.setCpf(CPF);
                    cadastro.setEmail(EMAIL);
                    cadastro.setLogin(0);

                    Banco.AdicionarCadastro(cadastro);
                    LimparCampos();
                    
                    dispose();
                    
                   interfaceLogin login = new interfaceLogin();
                   login.setVisible(true);
                    
         
              }catch(SQLException e){
                  JOptionPane.showMessageDialog(null,"ERROR: "+e.getMessage(),"ERROR",JOptionPane.ERROR_MESSAGE);
              }
              

            }
            else{
                JOptionPane.showMessageDialog(null,"Senha Incorreta","ERROR",JOptionPane.ERROR_MESSAGE);
            }
        }catch(HeadlessException e){
            JOptionPane.showMessageDialog(null,"Error: "+e.getMessage(),"ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

