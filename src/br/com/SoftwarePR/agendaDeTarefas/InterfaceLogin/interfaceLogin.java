    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        
        
        String usuario = txtUsuario.getText();
        String senha = txtSenha.getText();
        String nomeUsuario = null;
        int primeiroAcesso = 0;
        
        conexaoBanco Banco = new conexaoBanco();
        
        if(usuario.isEmpty() || senha.isEmpty()){
            JOptionPane.showMessageDialog(null,"Preencha os Campos de Usu치rio e Senha...","ERROR",JOptionPane.ERROR_MESSAGE);
        }else{
          
           
            try {
                List<construtorNomeLogin> lista = Banco.Login(usuario, senha);
                 
                
                
                
                if(lista != null){
                    for (construtorNomeLogin linha : lista){
                    nomeUsuario = linha.getNome();
                    primeiroAcesso = linha.getPrimeiroAcesso();
                    }
                        if(nomeUsuario != null){

                            if(primeiroAcesso == 0){
                                String fraseI = """
                                               Primeiro Acesso na Plataforma. 
                                               Por favor Alterar a Senha!
                                               """ + nomeUsuario;
                                int retorno = JOptionPane.showConfirmDialog(null,fraseI,"Primeiro Acesso",JOptionPane.OK_OPTION);

                                if(retorno == 1){
                                   dispose();

                                }else{
                                    interfaceTrocarSenha trocarSenha = new interfaceTrocarSenha();
                                    trocarSenha.setVisible(true);
                                    dispose();
                                }
                        }else{
                                
                                String frase = """
                                              Bem Vindo 
                                              """ +nomeUsuario;
                                JOptionPane.showMessageDialog(null,frase,"Bem Vindo",JOptionPane.NO_OPTION);
                                interfaceMenuTarefa menu = new interfaceMenuTarefa();
                                menu.setVisible(true);
                                dispose();
                        }

                    }
                    
                }else{
                    JOptionPane.showMessageDialog(null,"Usu치rio ou Senha Inv치lidos !","ERROR",JOptionPane.ERROR_MESSAGE);
                }
             
            } catch (SQLException ex) {
                Logger.getLogger(interfaceLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        }
        
        
    }//GEN-LAST:event_btnLoginActionPerformed

