    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        
        try{
            DefaultTableModel dados = (DefaultTableModel) Tabela.getModel();
            int numeroLinha = Tabela.getSelectedRow();


            if(numeroLinha !=-1 ){

                int id = Integer.parseInt(dados.getValueAt(numeroLinha,0).toString());
                construtorCriarTarefa Tarefa = new construtorCriarTarefa();
                Tarefa.setId(id);
                conexaoBanco Banco = new conexaoBanco();
                try {
                    Banco.DeletarTarefa(Tarefa);
                } catch (SQLException ex) {
                    Logger.getLogger(interfaceDeletarTarefa.class.getName()).log(Level.SEVERE, null, ex);
                }

            }else{
                JOptionPane.showMessageDialog(null,"Escolha uma ID para Excluir !","ERROR de ID",JOptionPane.ERROR_MESSAGE);

            }
        }catch(HeadlessException | NumberFormatException e){
            JOptionPane.showMessageDialog(null,"ERROR: "+e.getMessage(),"ERRO",JOptionPane.ERROR_MESSAGE);
        }
            
        
        
    }//GEN-LAST:event_btnExcluirActionPerformed

