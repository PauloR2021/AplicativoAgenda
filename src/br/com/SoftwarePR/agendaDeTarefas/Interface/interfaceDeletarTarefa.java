    public void carregarTarefas(){
        
        conexaoBanco Banco = new conexaoBanco();
        
        List<construtorCriarTarefa> lista = Banco.ListarTarefas();
        
        DefaultTableModel dados = (DefaultTableModel) Tabela.getModel();
        
        dados.setNumRows(0);
        
        for(construtorCriarTarefa linha : lista){
            dados.addRow(new Object[]{
                linha.getId(),
                linha.getNome(),
                linha.getDescricao(),
                linha.getData(),
                linha.getStatus()
            });
            
        }
        
        
    }
    
    
    public void carregarTarefasBusca(){
        
        try{
            String busca = txtPesquisa.getText();
            conexaoBanco Banco = new conexaoBanco();

            List<construtorCriarTarefa> lista = Banco.ListarTarefasBuscar(busca);

            DefaultTableModel dados = (DefaultTableModel) Tabela.getModel();

            dados.setNumRows(0);

            for(construtorCriarTarefa linha : lista){
                dados.addRow(new Object[]{
                    linha.getId(),
                    linha.getNome(),
                    linha.getDescricao(),
                    linha.getData(),
                    linha.getStatus()
                });

            }
        
        }catch(Exception e){
            System.out.println(e);
        }
        
        
    }


